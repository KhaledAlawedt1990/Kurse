using System;

namespace MyFirstClassLibrary
{
    public class clsRechenOperation
    {
        public float Sum(float x, float y)
        {
            return x + y;
        }

        public float Sum(float x, float y, float z)
        {
            return x + y + z;
        }
        public float Sum(float x, float y, float z , float w)
        {
            return x + z + y + w;
        }

